// ---------------------------------------------------------------------------
// Primitive aliases
// ---------------------------------------------------------------------------

export type Rank = 'Queen' | 'King' | 'Ace' | 'Jack';
export type Persona = 'Novice' | 'Reader' | 'Misdirector' | 'Silent';
export type TruthState = 'honest' | 'lying';
export type JokerType =
  | 'poker_face'
  | 'stage_whisper'
  | 'earful'
  | 'cold_read'
  | 'second_wind';

// ---------------------------------------------------------------------------
// Card
// ---------------------------------------------------------------------------

export interface Card {
  /** Stable per session — `${rank}-${i}` where i ∈ 0..4. e.g. `Queen-0`, `King-3`. Generated by `deck-and-claims` / `makeDeck()`. */
  id: string;
  rank: Rank;
}

// ---------------------------------------------------------------------------
// Voice (owned by voice-tell-taxonomy spec; re-exported here for FSM use)
// ---------------------------------------------------------------------------

/**
 * Matches ElevenLabs `voiceSettings` payload for Flash v2.5. Property names
 * are snake_case to mirror the API — we pass this verbatim into the
 * `voiceSettings` field of `textToSpeech.convert()` after a shape adapter
 * (SDK uses camelCase on its typed surface).
 */
export interface VoiceSettings {
  stability: number; // [0, 1]
  similarity_boost: number; // [0, 1]
  style: number; // [0, 1]
  speed: number; // ~[0.9, 1.1]
}

export interface VoiceMeta {
  latencyMs: number;
  fillerCount: number;
  pauseCount: number;
  speechRateWpm: number;
  /** 0..1 normalized. See voice-tell-taxonomy spec §3. */
  lieScore: number;
  /** null = unparseable — triggers the caller-side retry UI. */
  parsed: { count: number; rank: Rank } | null;
}

// ---------------------------------------------------------------------------
// Claims (server-side full; client-side public projection)
// ---------------------------------------------------------------------------

export interface Claim {
  by: 'player' | 'ai';
  /** Always 1 or 2 (spec §1.3). */
  count: number;
  claimedRank: Rank;
  /** SERVER-ONLY — stripped by toClientView for opponent's view. */
  actualCardIds: string[];
  /** Derived server-side from actualCardIds vs claimedRank. */
  truthState: TruthState;
  /** Player claims only. */
  voiceMeta?: VoiceMeta;
  /** AI claims only. */
  ttsSettings?: VoiceSettings;
  /** AI claims only — preserved on own-viewer side for Earful autopsy UI. */
  llmReasoning?: string;
  /** Optional dialogue variant the TTS layer spoke. */
  claimText?: string;
  /**
   * AI claims only — the voice-tell preset active on this TTS turn. Day-5
   * pre-land (joker-system spec §7.4.3 Earful autopsy). Read by the reducer
   * when populating `ClientSession.autopsy` on `ChallengeWon` while Earful
   * is in `activeJokerEffects`. Populated by the AI claim construction site
   * (ai-opponent worktree Day-5).
   */
  voicePreset?: VoiceTellPreset;
  timestamp: number;
}

export interface PublicClaim {
  by: 'player' | 'ai';
  count: number;
  claimedRank: Rank;
  claimText?: string;
  timestamp: number;
  /**
   * Cold Read projection only (joker-system §7.4.2, Task 12).
   * Retained by toClientView on the LAST AI claim when `cold_read` is in
   * `Round.activeJokerEffects`. Stripped on all other claims + all non-Cold-Read rounds.
   * Strictly additive — does not affect any existing PublicClaim construction site.
   *
   * Spec-faithful narrow shape: only `lieScore` crosses the wire. Other
   * VoiceMeta fields (latencyMs, fillerCount, etc.) are server-only and MUST
   * NOT be projected to the client. `buildContexts.ts` carries the full
   * VoiceMeta server-side via an explicit `Omit<PublicClaim,'voiceMeta'> &
   * { voiceMeta?: VoiceMeta }` intersection, independent of this narrow shape.
   */
  voiceMeta?: { lieScore: number };
}

