/**
 * Produced by `deck-and-claims` / `dealFresh()`, consumed by the reducer via
 * SetupComplete and JokerPicked events. Caller owns all randomness.
 *
 * Invariant: playerHand (5) + aiHand (5) + remainingDeck (10) === all 20
 * unique Cards.
 */
export interface RoundDeal {
  playerHand: Card[];
  aiHand: Card[];
  remainingDeck: Card[];
  targetRank: Rank;
  activePlayer: 'player' | 'ai';
}

// ---------------------------------------------------------------------------
// GameEvent discriminated union
// ---------------------------------------------------------------------------
//
// Every event carries `now` so the reducer never reads Date.now() directly.
// SetupComplete and JokerPicked carry caller-provided deals so randomness
// stays out of the reducer too.

export type GameEvent =
  | {
      type: 'SetupComplete';
      now: number;
      initialDeal: RoundDeal;
      musicTracks: MusicTrack[];
      /**
       * Day-5 pre-land (joker-system spec §7.1.3). Optional — if omitted,
       * the reducer default-seeds via `seedDrawPile()` (5 types × 3 copies
       * = 15 jokers). Backward-compatible with existing SetupComplete tests.
       */
      initialJokerDrawPile?: JokerType[];
    }
  | { type: 'ClaimMade'; claim: Claim; now: number }
  | { type: 'ClaimAccepted'; now: number }
  | { type: 'ChallengeCalled'; now: number }
  | { type: 'RevealComplete'; challengeWasCorrect: boolean; now: number }
  | { type: 'RoundSettled'; now: number }
  | {
      type: 'JokerPicked';
      joker: JokerType;
      nextRoundDeal: RoundDeal;
      now: number;
    }
  | { type: 'JokerOfferSkippedSessionOver'; now: number }
  | {
      type: 'Timeout';
      kind: 'active_player';
      cardIdToPlay: string;
      now: number;
    }
  | { type: 'Timeout'; kind: 'responder'; now: number }
  // -------------------------------------------------------------------------
  // Day-5 pre-land (joker-system + probe-phase). Reducer stubs in fsm.ts
  // throw InvalidTransitionError with a "pending implementation" marker
  // until the corresponding worktree fills in the transition logic.
  // -------------------------------------------------------------------------
  /**
   * joker-system spec §7.1.1 — offer a 1-of-3 joker selection to round winner.
   * `offered` length 1..3 (shrinks on pile exhaustion tail). `newDrawPile` is
   * the caller-computed updated draw pile after deduplication-by-type and
   * offer-slice removal.
   */
  | {
      type: 'JokerOffered';
      offered: JokerType[];
      newDrawPile: JokerType[];
      now: number;
    }
  /**
   * joker-system spec §7.1.1 — pile empty at offer time. Reducer transitions
   * directly to next round via caller-provided `nextRoundDeal`. Distinct from
   * `JokerOfferSkippedSessionOver` (session-over case).
   */
  | { type: 'JokerOfferEmpty'; nextRoundDeal: RoundDeal; now: number }
  /**
   * joker-system spec §7.1.1 — activate a held joker. `second_wind` is
   * NEVER used here — it auto-consumes on strike events. See spec §5.
   */
  | { type: 'UseJoker'; joker: JokerType; by: 'player' | 'ai'; now: number }
  /** probe-phase spec §7.1 — begin a Stage Whisper probe; sets Round.activeProbe. */
  | { type: 'ProbeStart'; probe: ActiveProbe; now: number }
  /**
   * probe-phase spec §7.1 — probe reveal acknowledged. Owned by joker-system
   * (declares the type) but CONSUMED by probe-phase reducer slice that
   * clears Round.activeProbe.
   */
  | { type: 'ProbeComplete'; whisperId: string; now: number }
  /** probe-phase spec §7.1 — probe timeout. Clears Round.activeProbe. */
  | { type: 'ProbeExpired'; whisperId: string; now: number };

// ---------------------------------------------------------------------------
// Error class
// ---------------------------------------------------------------------------

/**
 * Thrown by the reducer when an event isn't valid in the current Session /
 * Round state (e.g. ClaimAccepted while in `claim_phase`, any event after
 * `session_over`). Never silently no-op.
 */
export class InvalidTransitionError extends Error {
  constructor(
    public readonly currentState: string,
    public readonly eventType: string,
  ) {
    super(
      `Invalid transition: event '${eventType}' not valid in state '${currentState}'`,
    );
    this.name = 'InvalidTransitionError';
  }
}
